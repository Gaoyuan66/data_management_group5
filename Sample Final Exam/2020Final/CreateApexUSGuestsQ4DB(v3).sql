CREATE TABLE IF NOT EXISTS hotel (
    `hotelID` 	INT 			PRIMARY KEY,
    `hotelName` VARCHAR(30)		NOT NULL	UNIQUE,
    `city` 		VARCHAR(20)		NOT NULL,
    `country` 	VARCHAR(20)		NOT NULL
);

CREATE TABLE IF NOT EXISTS roomType (
    `typeID` 	INT 			PRIMARY KEY,
    `typeName` 	VARCHAR(30)		NOT NULL	UNIQUE,
    `numBeds` 	INT 			NOT NULL,
    `balcony` 	INT 			NOT NULL
);

CREATE TABLE IF NOT EXISTS room (
    `roomNo` 	INT,
    `hotelID` 	INT,
    `typeID` 	INT,
    `price` 	INT				NOT NULL,
    CONSTRAINT pk_roomNo PRIMARY KEY (roomNo,hotelID),
    CONSTRAINT fk_roomType FOREIGN KEY (typeID) REFERENCES roomType (typeID),
    CONSTRAINT fk_hotelID FOREIGN KEY (hotelID) REFERENCES hotel (hotelID)
);

CREATE TABLE IF NOT EXISTS guest (
    `guestID` INT				PRIMARY KEY,
    `lastName` VARCHAR(11)		NOT NULL,
    `firstName` 	VARCHAR(11)		NOT NULL,
    `street` 	VARCHAR(29)		NOT NULL,
    `city` 		VARCHAR(20)		NOT NULL,
    `state` 	VARCHAR(2)		NOT NULL,
    `zip` 		INT				NOT NULL,
    `ApexChoice` 	INT			NOT NULL,
    CONSTRAINT uc_guest UNIQUE (firstName,lastName,street,city,zip)
);


CREATE TABLE IF NOT EXISTS booking (
    bookingID INT PRIMARY KEY auto_increment,
    `guestID` 	INT,
    `hotelID` 	INT,
    `roomNo` 	INT,
    `dateFrom` 	DATE			NOT NULL,
    `dateTo` 	DATE			NOT NULL,
    `discount` 	NUMERIC(3, 2)	DEFAULT 0.00,
    -- CONSTRAINT pk_booking PRIMARY KEY (guestID,hotelID,roomNo,dateFrom),
    CONSTRAINT fk_room FOREIGN KEY (hotelID, roomNo) REFERENCES room (hotelID, roomNo),
    CONSTRAINT fk_guest FOREIGN KEY (guestID) REFERENCES guest (guestID),
    CONSTRAINT uc_room_booking UNIQUE (hotelID,roomNo,dateFrom),
	CONSTRAINT chk_date CHECK(dateTo > dateFrom),
    CONSTRAINT chk_discount CHECK(discount >= 0 AND discount < 1.0)
);

